let currentLanguage = localStorage.getItem('app_language') || 'ru'

// –§—É–Ω–∫—Ü–∏—è –¥–ª—è –ø–æ–ª—É—á–µ–Ω–∏—è –ø–µ—Ä–µ–≤–æ–¥–∞
function t(key) {
	return translations[currentLanguage]?.[key] || translations.ru[key] || key
}

// –§—É–Ω–∫—Ü–∏—è –¥–ª—è –ø–µ—Ä–µ–∫–ª—é—á–µ–Ω–∏—è —è–∑—ã–∫–∞
function setLanguage(lang) {
	if (translations[lang]) {
		currentLanguage = lang
		localStorage.setItem('app_language', lang)
		updateAllTexts()
	}
}

// –§—É–Ω–∫—Ü–∏—è –¥–ª—è –æ–±–Ω–æ–≤–ª–µ–Ω–∏—è –≤—Å–µ—Ö —Ç–µ–∫—Å—Ç–æ–≤ –Ω–∞ —Å—Ç—Ä–∞–Ω–∏—Ü–µ
function updateAllTexts() {
	// –û–±–Ω–æ–≤–ª—è–µ–º —Ç–µ–∫—Å—Ç—ã –≤ HTML —ç–ª–µ–º–µ–Ω—Ç–∞—Ö —Å data-i18n –∞—Ç—Ä–∏–±—É—Ç–æ–º
	document.querySelectorAll('[data-i18n]').forEach(element => {
		const key = element.getAttribute('data-i18n')
		element.textContent = t(key)
	})
	
	// –û–±–Ω–æ–≤–ª—è–µ–º —Ç–µ–∫—Å—Ç—ã —ç—Ç–∞–∂–µ–π —Å data-i18n-floor –∞—Ç—Ä–∏–±—É—Ç–æ–º
	document.querySelectorAll('[data-i18n-floor]').forEach(element => {
		const floorNum = element.getAttribute('data-i18n-floor')
		const floorKey = `floor${floorNum}`
		element.textContent = `üè¢ ${t(floorKey)}`
	})
	
	// –û–±–Ω–æ–≤–ª—è–µ–º placeholder'—ã
	document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
		const key = element.getAttribute('data-i18n-placeholder')
		element.placeholder = t(key)
	})
	
	// –û–±–Ω–æ–≤–ª—è–µ–º title –∞—Ç—Ä–∏–±—É—Ç—ã
	document.querySelectorAll('[data-i18n-title]').forEach(element => {
		const key = element.getAttribute('data-i18n-title')
		element.title = t(key)
	})
	
	// –í—ã–∑—ã–≤–∞–µ–º —Ñ—É–Ω–∫—Ü–∏–∏ –æ–±–Ω–æ–≤–ª–µ–Ω–∏—è –¥–ª—è –¥–∏–Ω–∞–º–∏—á–µ—Å–∫–æ–≥–æ –∫–æ–Ω—Ç–µ–Ω—Ç–∞
	if (typeof updateDynamicTexts === 'function') {
		updateDynamicTexts()
	}
}

